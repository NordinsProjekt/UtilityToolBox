namespace Services.Factories.Types;

/// <summary>
/// Specifies the display direction for a menu.
/// </summary>
public enum MenuDirection
{
    /// <summary>
    /// Menu items are displayed horizontally (side by side).
    /// </summary>
    Horizontal = 0,
    
    /// <summary>
    /// Menu items are displayed vertically (one per line).
    /// </summary>
    Vertical = 1,
}